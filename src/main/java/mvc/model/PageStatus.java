package mvc.model;

public class PageStatus {
	
	String msg, url; // 메시지랑 url을 받기 위한 변수
	
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
}
